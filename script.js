// State logic
let currentLang = 'en';

function setLanguage(lang) {
    if (lang !== 'en' && lang !== 'so') lang = 'en';
    currentLang = lang;
    localStorage.setItem('backhome_lang', lang);
    
    document.documentElement.lang = lang;
    
    // Toggle active state on buttons
    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        if (btn.innerText.toLowerCase() === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });

    // Translate DOM nodes
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[key] && translations[key][lang]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[key][lang];
            } else {
                el.innerHTML = translations[key][lang]; // innerHTML safely allows our <br> and <span>
            }
        }
    });
}

// Initialization
document.addEventListener('DOMContentLoaded', () => {
    // Load language preference
    const savedLang = localStorage.getItem('backhome_lang') || 'en';
    setLanguage(savedLang);

    // Form logic for Waitlist
    const waitlistForm = document.getElementById('form');
    if (waitlistForm) {
        waitlistForm.addEventListener('submit', (e) => {
            e.preventDefault();
            const btn = waitlistForm.querySelector('button[type="submit"]');
            const originalText = btn.innerHTML;
            btn.textContent = currentLang === 'so' ? 'Kugusoo dhawow liiska 🎉' : 'Joined Waitlist 🎉';
            btn.style.backgroundColor = '#4caf50';
            
            setTimeout(() => {
                btn.innerHTML = originalText;
                btn.style.backgroundColor = '';
                waitlistForm.reset();
            }, 3000);
        });
    }

    // City chips selection logic
    const cityChips = document.querySelectorAll('.city-chip');
    const selectedCityInput = document.getElementById('selected-city');
    
    cityChips.forEach(chip => {
        chip.addEventListener('click', () => {
            // If already active, deselect
            if(chip.classList.contains('active')) {
                chip.classList.remove('active');
                if(selectedCityInput) selectedCityInput.value = '';
                return;
            }
            
            // Remove active class from all chips
            cityChips.forEach(c => c.classList.remove('active'));
            // Add active class to the clicked chip
            chip.classList.add('active');
            // Update hidden input
            if(selectedCityInput) selectedCityInput.value = chip.dataset.city;
        });
    });

    // Smooth scrolling for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                e.preventDefault();
                targetElement.scrollIntoView({ behavior: 'smooth' });
            }
        });
    });
});
